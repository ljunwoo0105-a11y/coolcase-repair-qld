import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get('LOVABLE_API_KEY');
    
    if (!LOVABLE_API_KEY) {
      throw new Error('LOVABLE_API_KEY is not configured');
    }

    const systemPrompt = `You are a helpful customer support assistant for CCR Tech Repair, a phone and device repair shop in Brisbane, Australia.

SERVICES WE OFFER:
- Phone Repair (Screen replacement, battery replacement, charging port repair, water damage repair)
- Tablet Repair (All brands and models)
- Computer Repair (Hardware and software issues)
- Drone Repair (All models)
- IT Solutions (Network setup, data recovery, virus removal)

PRICING:
- Starting from $29 for basic repairs
- Free diagnostics and quotes
- Same-day service available
- Genuine quality parts
- Lifetime warranty on all repairs

CONTACT INFO:
- Phone: 0452 385 321
- Location: Brisbane, Australia
- Hours: Monday-Saturday, 9 AM - 6 PM

IMPORTANT GUIDELINES:
- Be friendly, professional, and helpful
- Answer questions about pricing, services, and repair times
- If asked for specific repair pricing, remind them we offer free quotes
- For complex issues, suggest they call or visit for a free diagnostic
- We have certified technicians and are fully insured
- We use genuine quality parts with lifetime warranty

Keep responses concise and helpful. If you don't know something specific, suggest they contact us directly.`;

    const response = await fetch('https://ai.gateway.lovable.dev/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${LOVABLE_API_KEY}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'google/gemini-2.5-flash',
        messages: [
          { role: 'system', content: systemPrompt },
          ...messages
        ],
        stream: false,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: 'Rate limit exceeded. Please try again in a moment.' }),
          { status: 429, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: 'Service temporarily unavailable. Please try again later.' }),
          { status: 402, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
        );
      }
      
      const errorText = await response.text();
      console.error('AI gateway error:', response.status, errorText);
      throw new Error('AI service error');
    }

    const data = await response.json();
    const assistantMessage = data.choices?.[0]?.message?.content;

    return new Response(
      JSON.stringify({ message: assistantMessage }),
      { headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );

  } catch (error) {
    console.error('Chat error:', error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : 'Unknown error' }),
      { status: 500, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );
  }
});
